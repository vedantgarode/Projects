<h3  style="text-shadow:2px 3px 4px;">JODY ROBERTS</h3>
<p>I began my journey at Little Dolphins 22 years ago as the parent of a 4-year-old, and in the span of that year, I fell in love with this way of early childhood education. With a professional background in the arts, I knew I wanted to be part of a community that nurtures a child’s creativity and imagination through art, music and folk tales from around the world. It was inspiring to see that a rich arts-based curriculum along with the immersion into a different culture each month had a profound impact on children at this tender age; giving them a sense of wonder, curiosity, and respect for others. Throughout the ensuing years, I have dedicated myself to continuing this hands-on approach to education by developing an after school enrichment program, teaching art classes, and acting as art consultant to keep the aesthetic vision of the school alive and growing. I believe that as early childhood educators, we have the opportunity to create an excitement for learning that lasts a lifetime. My 4 year old Little Dolphin is now 26 years old and she credits her confidence and ability to think creativity to her experience all those years ago at Little Dolphins By The Sea.
</p>
<p>
I received a B.A. in fine arts from Binghamton University, New York. I have worked in the areas of textile design, photography and children’s art education and co-authored a children’s art book, titled P is for Peanut, for the Getty Museum. I am the mother of two grown children (the most fulfilling job!) and live in Pacific Palisades with my husband, David Schwartz, and dog, Banjo.</p>
