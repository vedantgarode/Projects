<h1>MISSION & PHILOSOPHY</h1>
<h3>AN EXPERIENTIAL LEARNING AND RESEARCH-BASED, INDIVIDUALIZED APPROACH TO EARLY CHILDHOOD AND BEYOND</h3>
<p>The Seven Arrows Way of Education acknowledges children as strong, capable, resilient, and full of wonder and knowledge. Every child brings with him or her a deep curiosity and potential. We combine an appreciation of beauty and multiculturalism with a disciplined academic program rooted in math and science. Our goal is to help children become trusting, independent thinkers who display self-knowledge, respect, and empathy for other people. In early childhood, play is the best way to harness the learning power of children, stimulate creativity, and heighten retention.
</p>
<p>
The Seven Arrows Way is based on the profound pedagogy and philosophy of Experiential Learning. We ground our educational approach in leading-edge brain research. The Theory of Multiple Intelligences, developed at Harvard University by cognitive scientist Dr. Howard Gardner, holds that different people have diverse ways of learning, remembering, performing, processing, and understanding information. Gardner posits that there are seven distinct intelligences: Visual-Spatial, Bodily-Kinesthetic, Musical, Interpersonal, Intrapersonal, Linguistic, and Logical-Mathematical. Our curriculum is designed to explore these intelligences, and to identify each child’s individual learning style, in order to optimize his or her educational results.
</p><p>
We also draw upon Dr. Richard Davidson’s research in affective neuroscience, which focuses on how a child’s emotional state affects the way he or she absorbs information. He enumerates six significant factors determining learning success:
</p>
<p>
<ul>
<li>Resilience: how slowly or quickly one recovers from adversity.</li>
<br>
<li>Outlook: how long one is able to sustain positive emotion.
</li><br>
<li>
Social Intuition: how adept one is at picking up social signals from those around you.
</li><br>
<li>
Self-Awareness: how alert one is to the physical signals that accompany emotional states.
</li><br>
<li>
Sensitivity to Context: how good one is at regulating emotional responses depending on environment.
</li><br>
<li>
Attention: how sharp and clear one’s focus is.
</li>
</ul>
<p>
We employ Davidson’s strategies to identify each child’s unique patterns, helping them to become aware of themselves and of others and readying them to learn. We continue to refine our practices by actively engaging with a wide variety of experts in the field, including Dr. Carol Dweck, Dr. Martin E.P. Seligman, and Dr. Daniel Goldman. We strive to recognize what makes each of our children unique and foster their strengths and address their weaknesses accordingly.
</p>
<p>
We are a community of lifelong learners, and encourage teachers and parents to continue their own growth. We foster this with ongoing professional development for our teachers and a robust expert speaker series and parenting library for our parents.
</p>
