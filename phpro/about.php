<h3 style="text-shadow:2px 3px 4px;">MARI-JO DEUTSCHMAN</h3>
<p>Ever since I can remember, I wanted to be part of the educational process. From an early age, I taught, coached, tutored and worked with people of all ages, working to inspire them to be their best self in the classroom, on a court and as a person. I was and still am a voracious reader, an inspired student and a passionate athlete. I had teachers, mentors and coaches who from an early age made an impact on my life thus I too am determined to inspire others to find their passion.
</p>
<p>
While educating our children is a serious and important intellectual endeavor, I am a true believer that there always needs to be an element of fun in the process. Fun does not always mean laughing, giggling or playing, to me the fun is feeling good about the process, being present and enjoying the moment. That is how one learns. It might take 1, 2 or 50 tries to grasp a skill, but the enjoyment is derived from the feeling of satisfaction that you are giving your best effort.</p>
<p>
Teaching by example is a very important educational tool. Everyday at Little dolphins I learn something new from my co founders, from our teachers, and from our young students. As a founder, I feel it is important to set the example of being an eternal student.</p>
<p>
One must never forget that the process of learning at any age is an adventure, a way of opening the mind and soul to new experiences. When you engage children at an early age, you lay a foundation, which stimulates and inspires curiosity and provides a solid foundation for lifelong learning.</p>
