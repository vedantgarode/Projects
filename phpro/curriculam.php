<h2>VISUAL ARTS</h2>
<h3>ART PROJECTS AS MECHANISMS FOR LEARNING</h3>
<p>Our curriculum incorporates amazing art projects as mechanisms for learning. Through “making,�? children become directly engaged with what they are learning and fully immersed in the subject at hand. By using the works of famous artists like: Miro, Chagall, Matisse, Munch, Picasso, Seurat, Calder, Braque, Kahlo, Van Gogh, Gauguin, Monet – to name a few – as educational points of departure, children are acquainted with subjects and then become immersed in themes typically associated with a higher age level of education. Through painting, collage, water-color, sketching, building, and construction – the children’s spatial awareness and sensitivity are triggered and a deeper level of intellectual and thematic thinking can be reached.</p>
<h2>MUSIC</h2>
<h3>MOVEMENT AND MUSIC ARE INTEGRAL FACETS OF OUR CURRICULUM</h3>
<p>
During our music and musical theatre classes, we introduce the basic components of theatrical expression while teaching the songs and performances are part of our daily circle time and our monthly festivals. Through storytelling and puppetry we teach them to express themselves with a combination of movement and music. Kids process information as they move; they learn letters, which become the rhythm of their larger motor-skills as they move in time and in harmony with their bodies. This supports thinking and language acquisition, as movement is a great way to access one’s working memory and encode new information in one’s long-term memory. When children connect certain movements with musical notes, they are able to memorize words more easily in another languages, as well as in English. Therefore, movement and music are very much tied together in our modes of instruction and are integral facets of our curriculum and educational programs.</p>
