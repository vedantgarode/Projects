<h1>THE ARTS</h1>
<h3>FOSTERING UNDERSTANDING THROUGH CULTURAL ENRICHMENT</h3>
<p>Little Dolphins By The Sea is a preschool that recognizes that the arts are fundamental to the development of children. It was founded to promote early imaginative learning through hands-on experiences in visual art, theatre, and music for the purpose of fostering understanding through cultural enrichment.</p>
