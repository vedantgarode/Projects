<h1>MOVEMENT</h1>
<h3>AN ESSENTIAL MECHANISM FOR EXPRESSING FEELINGS AND COMMUNICATING</h3>
<p>
Another special area that Little Dolphins focuses on is movement. Movement is an essential mechanism for expressing feelings and communicating. This part of the curriculum supports both physical and emotional learning and is essential for early development. When children learn how to be within a space, it not only promotes coordination, but also the creation of new sensory bodily connections. Our outdoor campus is an important part of our movement curriculum program as it allows our children to move freely and with autonomy.</p>
<p>
Movement is allowed in all our instruction and during unstructured inside time. It is imperative to provide room and space that allow kids to move and to find themselves by becoming one with their bodies. This is necessary not only for the self-regulation of their bodies, but also for how they learn to express themselves through language and emotion. Encouraging movement is central to our teaching methodology.</p>
