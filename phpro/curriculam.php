<h1>ACADEMICS<h1>
<h3>ALLOWING CHILDREN TO GROW SOCIALLY, EMOTIONALLY, PHYSICALLY AND INTELLECTUALLY</h3>
<p>Our preschool program emphasizes developmentally appropriate activities that allow children to grow socially, emotionally, physically, and intellectually. Our Academic time is spent in self-directed and meaningful creative play, based on a child's individual needs, as this builds the important foundational pillars for the future. Because learning begins with the senses—a child gathers meaning from the world through the experiences of touching, hearing, smelling, seeing, and tasting—our campus is a rich and stimulating, but never overwhelming, environment. The classroom has an open structure in which a child is free to choose individual areas of interest. All activities and learning are based on play.</p>
