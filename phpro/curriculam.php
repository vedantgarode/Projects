<h1>TRANSITIONAL KINDERGARTEN (T.K.)</h1>
<h3>A MULTICULTURAL CURRICULUM WITH A HANDS-ON APPROACH</h3>
<p>
Our purpose is to provide an approach for the development of academic skills by focusing on the whole child, which means taking into account each of their personalized needs. The TK program is five days a week from 8:45 a.m.– 3:00 p.m. for children ages 4.9 – 5.2 (four years and nine months to five years and two months) who may be in a transitional year between preschool and kindergarten.</p>
<p>
In the tradition of education implemented in the Little Dolphins By the Sea preschool program, the TK program offers a multicultural curriculum with a hands-on approach, designed to develop each child’s multiple intelligences. Our comprehensive educational program includes: language arts, math, science, computers, music, art, drama, physical education and movement. We strongly encourage parents of our students to become involved in every aspect of school life to make the student's educational experience that much more meaningful.</p>
<p>
We take a constructivist approach when it comes to our math and science curriculum, knowing that our students already have a substantial knowledge of the natural world. So when we teach math and science, the emphasis is that it is done through a hands-on exploration of art and play, which is a teaching methodology that is commonplace in the Maker’s Space community.
</p>
<p>
It is in the early childhood classroom where this kind of experience with the natural world must take place, allowing children to build experiences in investigation and problem solving and the foundation for understanding basic science concepts. This early science exploration can be a rich context in which children can use and develop other important skills including working with one another, basic large and small motor-controls, language learning, innovative thinking, and early mathematical understanding. Stimulating a child’s existing curiosity about the world s/he lives in, is a dynamic way to introduce new methodologies and encouraging that very curiosity that will make him or her an avid learner in the future.
</p>
