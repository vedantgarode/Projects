<p>
The developmental approach to education allows children to progress at an individual pace. We respect each child’s uniqueness and take the time to learn about every student as an individual, constantly examining every step of the education process to adapt to our students and their needs. While our curriculum does outline fundamental goals for the various subject areas, we allow flexibility for each child to master the various skills according to his or her own readiness. We do not expect all children to learn the same things in the same way on the same day.
</p><p>
For preschool age children, providing age-appropriate instruction encourages curiosity and exploration. They experience real phenomena in their environment and work on in-depth projects that combine multiple disciplines. It is important for young learners to interact with the real and tangible and have direct sensory experiences. Experts also maintain that young children learn better when they have social interaction and can discuss their work with their peers. By providing such opportunities, developmentally appropriate learning creates a sound base of knowledge that is more retainable, and also provides a strong foundation for abstract learning later in life.</p>
<p>
Our program goals and activities are designed for age appropriateness. Only by staying within such parameters can our children truly succeed and achieve the sense of accomplishment that is part of the foundation of their learning. We create a rich environment designed for hands-on learning. Monthly cultural themes support long­ term excitement and immersion into an area through music, art, and more. The children participate in an organized group activity, and then move on to learning centers that provide the child an opportunity to make independent choices of their activities. Children are naturally inquisitive and learn through exploration. Through our learning centers, our children can exercise choice and control in order to further develop their interests and explore the world they live in. This will further expand their understanding of their role in a democratic society and allow them to practice problem solving, experience immediate feedback, and grapple with real-world challenges.</p>
<p>
We believe that a direct learning experience – fostered in a thoughtfully prepared learning environment like ours – is in fact the most powerful teacher. Therefore, we place the highest importance on experiential learning, due to the fact that children are naturally inquisitive, active learners who teach themselves from their explorations of their world. Thus, our classrooms, the materials in them, and the outdoor breakout spaces that make-up our campus – an oasis in urban Santa Monica – are designed to fully exploit these innate tendencies in our children. These rich real-life experiences begin in the preschool, which is why the fostering of play in a safe environment is so important and is at the crux of our minds-on curriculum, and hands-on teaching philosophy.
</p><p>
This method of teaching requires input, time, and energy. It requires us to be flexible and improvise with immediate changes that are developmentally characteristic of children at this age.</p>
