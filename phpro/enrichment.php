				<p style="padding-left:30px;padding-right:30px;padding-bottom:30px;">(Optional for current Little Dolphins students)
				Children explore the exciting world of letters and numbers in this pre-kindergarten learning class, which becomes their first introduction to the academic world. The goal is to bring the alphabet to life; the class is designed to tap into each child's natural curiosity to learn and to have fun (play at this age) while doing it! The ABC Class provides a structured environment designed to ease the transition from preschool to transitional kindergarten, which not only helps children identify the alphabet and learn numbers, but also aids those who are developmentally ready to master the skill of reading and writing. This is done through teaching the whole language of learning. Children will learn to focus, have critical thinking, and strengthen their memory, which is a skill that will be critical in their learning.<br><br>
				Children should show an interest in learning about these areas before entering the class. This class is available to children ages 4 ½ years and up or to children who are developmentally ready for this program. The ABC Class hours are 1:00 p.m. – 2:45 p.m., Monday – Thursday.</p>
