				<h3>A RICH SET OF EXTRA-CURRICULAR ACTIVITIES AND COURSES</h3>
				<p>Roots & Wings started as an after school day care for pre-school families needing more childcare after the 1PM dismissal and eventually grew into a rich set of extra-curricular activities and courses up to 6th Grade level. Theatre, music, dance, Spanish, early academics, cooking, arts, and sports programming quickly became weekly after-school staples for our community of students and parents, with all offerings keeping in line with the Seven Arrows Way of Education. Currently, Little Dolphin’s Santa Monica campus offers the following enrichment classes for preschool students.</p>

				<p>Classes Include: Parent & Me, ABC Class, Alphabet Soup, Cooking and Spanish.</p>
				<p>
				The Seven Arrows Way is based on the profound pedagogy and philosophy of Experiential Learning. We ground our educational approach in leading-edge brain research. The Theory of Multiple Intelligences, developed at Harvard University by cognitive scientist Dr. Howard Gardner, holds that different people have diverse ways of learning, remembering, performing, processing, and understanding information. Gardner posits that there are seven distinct intelligences: Visual-Spatial, Bodily-Kinesthetic, Musical, Interpersonal, Intrapersonal, Linguistic, and Logical-Mathematical. Our curriculum is designed to explore these intelligences, and to identify each child’s individual learning style, in order to optimize his or her educational results.
				</p>
